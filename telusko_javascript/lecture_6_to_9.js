// Type Conversion and Type Coercion

// Type Conversion
let a = 10;
let b = 20;

console.log('a + b = ', a + b);
console.log('typeof a = ', typeof a, 'typeof b = ', typeof b);

// Type Coercion
let c = "10";
let d = 20;

console.log('c + d = ', c + d);
console.log('typeof c = ', typeof c, 'typeof d = ', typeof d);

// Type Conversion
let e = Number(c);
let f = String(d);

console.log('e = ', e, 'f = ', f);  

